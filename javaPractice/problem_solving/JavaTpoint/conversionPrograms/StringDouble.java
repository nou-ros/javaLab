public class StringDouble{
	public static void main(String args[]){
		// String to Double
		String str = "23.6";
		double d = Double.parseDouble(str);
		System.out.println(d);
		System.out.println(Double.toString(89.7));
		
		// Double to String
		System.out.println(String.valueOf(38.223));
		
	}
}
